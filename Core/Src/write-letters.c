void drawMatrixMap(const uint8_t mapa[])
{
    for (int i = 0; i < 8; i++)
    {
        int linha = 7 - i;

        GPIOC->ODR |= 0x00FF;
        GPIOB->ODR &= ~(0xFF << 8);

        uint16_t colunas = (uint16_t)mapa[linha] << 8;

        GPIOB->ODR = (GPIOB->ODR & 0x00FF) | colunas;

        GPIOC->ODR &= ~(1 << i);

        HAL_Delay(1);
    }
}

const uint8_t *getMap(char c)
{
    int tamanho = sizeof(characters) / sizeof(FonteMatriz);
    for (int i = 0; i < tamanho; i++)
    {
        if (characters[i].caractere == c)
        {
            return characters[i].bitmap;
        }
    }
    return NULL;
}

const uint8_t *getLetterMap(char letter)
{
    return getMap(letter);
}

void writeLetter(const char *letter)
{
    for (int i = 0; letter[i] != '\0'; i++)
    {
        const uint8_t *mapa = getMap(letter[i]);
        if (mapa != NULL)
        {
            for (int t = 0; t < 100; t++)
            {
                drawMatrixMap(mapa);
            }
        }
    }
}

uint8_t getColumn(const uint8_t mapa[8], int col)
{
    uint8_t coluna = 0;
    for (int i = 0; i < 8; i++)
    {
        if (mapa[i] & (1 << (7 - col)))
        {
            coluna |= (1 << i);
        }
    }
    return coluna;
}

void rotateAntiClockwise(uint8_t src[8], uint8_t dst[8])
{
    for (int i = 0; i < 8; i++)
    {
        dst[i] = 0;
        for (int j = 0; j < 8; j++)
        {
            if (src[j] & (1 << i))
            {
                dst[i] |= (1 << (7 - j));
            }
        }
    }
}

void scrollText(const char *texto)
{
    uint8_t buffer[200];
    int pos = 0;

    for (int k = 0; texto[k] != '\0'; k++)
    {
        const uint8_t *mapa = getMap(texto[k]);
        if (mapa != NULL)
        {
            for (int c = 0; c < 8; c++)
            {
                buffer[pos++] = getColumn(mapa, c);
            }
            buffer[pos++] = 0x00;
        }
    }

    for (int offset = 0; offset < pos - 8; offset++)
    {
        uint8_t frame[8];
        for (int i = 0; i < 8; i++)
        {
            frame[i] = buffer[offset + i];
        }

        uint8_t frameRot[8];
        rotateAntiClockwise(frame, frameRot);

        for (int t = 0; t < 5; t++)
        {
            drawMatrixMap(frameRot);
        }
    }
}
